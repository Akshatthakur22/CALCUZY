interface ToolJsonLdProps {
  schemas: object[]
}

export default function ToolJsonLd({ schemas }: ToolJsonLdProps) {
  return (
    <>
      {schemas.map((schema, index) => (
        <script
          key={index}
          type="application/ld+json"
          dangerouslySetInnerHTML={{ __html: JSON.stringify(schema) }}
        />
      ))}
    </>
  )
}
